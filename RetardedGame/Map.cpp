CoordinateStruct Map::render(SDL_Rect playerLoc){
	float xLoc = playerLoc.x / 1000;

	CoordinateStruct relativeCoord{ 0, 0 };

	int firstTexture=0;
	if (xLoc < 1){
		firstTexture = xLoc;
	}
	else if (xLoc > 0 && xLoc < (texture.size() - 1)){
		relativeCoord.x = 1;
		firstTexture = xLoc-1;
	}
	else if (xLoc >= texture.size()-1){
		relativeCoord.x = 2;
		firstTexture = texture.size() - 3;
	}


	for (int i = 0; i < 3; i++){
		SDL_Rect size = {((i) * 1000), 0, 1000, 1000 };

		SDL_RenderCopy(texture.at(firstTexture + i)->getRenderer(), texture.at(firstTexture + i)->getTexture(), NULL, &size); //render the sprite to the canvas

	}

	return relativeCoord;
}

